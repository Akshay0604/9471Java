public class AssignmentOperator{
public static void main(String args[]){
int num1=125;
int num2=24;
  num2=num1;
System.out.println("=output:"+num2);

num2+=num1;
System.out.println("=output:"+num2);

num2-=num1;
System.out.println("=output:"+num2);

num2*=num1;
System.out.println("=output:"+num2);

num2/=num1;
System.out.println("=output:"+num2);

num2%=num1;

}}
